fprintf('\n---------- Statistics ----------\n');
eff_idx=find(eff_mask); ang_eff=zeros(n_eff,1);
normal_eff=zeros(n_eff,3); stress_eff_vec=zeros(n_eff,3); z_surf_eff=zeros(n_eff,1);
