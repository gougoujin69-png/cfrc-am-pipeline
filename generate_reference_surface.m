function [cost,Ja,Jg,Jc,Jm]=cost_raw(c,Phi,dPhi_x,dPhi_y,sigma1,wt,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg)
% RAW cost - no normalization. Used for computing reference scales.
% Key difference from v7b: Jg and Jc NO LONGER divide by Lz^2
    n_pts=size(Phi,1);Kr=length(c);
    gx=dPhi_x*c;gy=dPhi_y*c;
    Ja=0;
    for i=1:n_pts
        nm=sqrt(gx(i)^2+gy(i)^2+1);
        dv=sigma1(i,1)*(-gx(i)/nm)+sigma1(i,2)*(-gy(i)/nm)+sigma1(i,3)*(1/nm);
        Ja=Ja+wt(i)*dv^2;
    end
    Ja=Ja/n_pts;
    % Geometry: mean squared coefficient displacement (NO Lz^2 division!)
    Jg=mean((c-c_geom).^2);
    % Curvature: mean squared weighted coefficient (NO Lz^2 division!)
    Jc=mean((curv_weight.*c).^2);
    % Manufacturing
    n_eval=size(dPe_x,1);
    gx_e=dPe_x*c;gy_e=dPe_y*c;slope_e=sqrt(gx_e.^2+gy_e.^2);
    fxx_e=d2Pe_xx*c;fyy_e=d2Pe_yy*c;fxy_e=d2Pe_xy*c;
    denom_e=1+gx_e.^2+gy_e.^2;
    H_e=((1+gy_e.^2).*fxx_e-2*gx_e.*gy_e.*fxy_e+(1+gx_e.^2).*fyy_e)./(2*denom_e.^(1.5));
    J_conc=mean(max(0,H_e-mfg.kappa_max).^2)/mfg.kappa_max^2;
    J_slp=mean(max(0,slope_e-mfg.tan_theta_max).^2)/mfg.tan_theta_max^2;
    J_cvp=mean(max(0,H_e))/mfg.kappa_max;
    Jm=mfg.w_concave*J_conc+mfg.w_slope*J_slp+mfg.w_convex_pref*J_cvp;
    cost=Ja+Jg+Jc+Jm;
end

function [cost,Ja_n,Jg_n,Jc_n,Jm_n]=cost_norm(c,Phi,dPhi_x,dPhi_y,sigma1,wt,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg,...
    sc,la,lg,lc,lm)
% NORMALIZED cost: each term / its reference scale
    [~,Ja_raw,Jg_raw,Jc_raw,Jm_raw]=cost_raw(c,Phi,dPhi_x,dPhi_y,sigma1,wt,...
        c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg);
    Ja_n=Ja_raw/sc.Ja_ref; Jg_n=Jg_raw/sc.Jg_ref;
    Jc_n=Jc_raw/sc.Jc_ref; Jm_n=Jm_raw/sc.Jm_ref;
    cost=la*Ja_n+lg*Jg_n+lc*Jc_n+lm*Jm_n;
end

function grad=grad_norm(c,Phi,dPhi_x,dPhi_y,sigma1,wt,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg,...
    sc,la,lg,lc,lm)
% NORMALIZED gradient
    Kr=length(c); grad=zeros(Kr,1);
    n_pts=size(Phi,1);
    % --- Alignment ---
    gx=dPhi_x*c;gy=dPhi_y*c;
    for i=1:n_pts
        gxi=gx(i);gyi=gy(i);nm2=gxi^2+gyi^2+1;nm=sqrt(nm2);
        s=sigma1(i,:);
        dv=s(1)*(-gxi/nm)+s(2)*(-gyi/nm)+s(3)*(1/nm);
        inv3=1/(nm*nm2);
        dd_gx=s(1)*(gxi^2-nm2)*inv3+s(2)*gxi*gyi*inv3+s(3)*gxi*inv3;
        dd_gy=s(1)*gxi*gyi*inv3+s(2)*(gyi^2-nm2)*inv3+s(3)*gyi*inv3;
        grad=grad+(la/sc.Ja_ref)*(2*wt(i)*dv*(dd_gx*dPhi_x(i,:)'+dd_gy*dPhi_y(i,:)'))/n_pts;
    end
    % --- Geometry ---
    grad=grad+(lg/sc.Jg_ref)*2*(c-c_geom)/Kr;
    % --- Curvature ---
    grad=grad+(lc/sc.Jc_ref)*2*(curv_weight.^2.*c)/Kr;
    % --- Manufacturing ---
    n_eval=size(dPe_x,1);
    gx_e=dPe_x*c;gy_e=dPe_y*c;slope_e=sqrt(gx_e.^2+gy_e.^2);
    fxx_e=d2Pe_xx*c;fyy_e=d2Pe_yy*c;fxy_e=d2Pe_xy*c;
    denom_e=1+gx_e.^2+gy_e.^2;
    H_e=((1+gy_e.^2).*fxx_e-2*gx_e.*gy_e.*fxy_e+(1+gx_e.^2).*fyy_e)./(2*denom_e.^(1.5));
    dH_dc=(d2Pe_xx+d2Pe_yy)/2;
    % Concave violation
    ce=max(0,H_e-mfg.kappa_max);
    if any(ce>0)
        grad=grad+(lm*mfg.w_concave/sc.Jm_ref)*2*dH_dc'*ce/(n_eval*mfg.kappa_max^2);
    end
    % Slope violation
    se=max(0,slope_e-mfg.tan_theta_max); as=se>0;
    if any(as)
        isl=zeros(n_eval,1);isl(as)=1./max(slope_e(as),1e-12);
        mult=2*se.*isl;
        grad=grad+(lm*mfg.w_slope/sc.Jm_ref)*(dPe_x'*(mult.*gx_e)+dPe_y'*(mult.*gy_e))/(n_eval*mfg.tan_theta_max^2);
    end
    % Convexity preference
    ap=H_e>0;
    if any(ap)
        grad=grad+(lm*mfg.w_convex_pref/sc.Jm_ref)*dH_dc'*double(ap)/(n_eval*mfg.kappa_max);
    end
end

function [u1,v1,w1,m1]=gauss3d(u,v,w,m,sigma)
    r=ceil(3*sigma);x=-r:r;k=exp(-x.^2/(2*sigma^2));k=k/sum(k);
    u1=convn(convn(convn(u,reshape(k,[],1,1),'same'),reshape(k,1,[],1),'same'),reshape(k,1,1,[]),'same');
    v1=convn(convn(convn(v,reshape(k,[],1,1),'same'),reshape(k,1,[],1),'same'),reshape(k,1,1,[]),'same');
    w1=convn(convn(convn(w,reshape(k,[],1,1),'same'),reshape(k,1,[],1),'same'),reshape(k,1,1,[]),'same');
    m1=convn(convn(convn(m,reshape(k,[],1,1),'same'),reshape(k,1,[],1),'same'),reshape(k,1,1,[]),'same');
end