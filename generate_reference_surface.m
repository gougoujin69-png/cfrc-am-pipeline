w_final=w_stress.*(1+BC_BOOST_FACTOR*w_bc);
wn=min(w_final);wx=max(w_final);
if wx-wn>1e-12,w_final=0.5+(w_final-wn)/(wx-wn);end

%% STEP C: OPTIMIZATION
fprintf('\n---------- Step C ----------\n');

% C1: Modes
modes=[];
for m=0:Mmax
    if sym_x&&mod(m,2)~=0,continue;end
    for n=0:Nmax
        if sym_y&&mod(n,2)~=0,continue;end
        modes(end+1,:)=[m,n];%#ok<AGROW>
    end
end
Kr=size(modes,1); fprintf('  %d modes\n', Kr);

mode_kappa=zeros(Kr,1);
for k=1:Kr, mode_kappa(k)=(modes(k,1)*pi/Lx)^2+(modes(k,2)*pi/Ly)^2; end
curv_weight=mode_kappa; if max(curv_weight)>0,curv_weight=curv_weight/max(curv_weight);end

% C2: Alignment basis
xi_sel=(coords(:,1)-x_min)/Lx; eta_sel=(coords(:,2)-y_min)/Ly;
Phi=zeros(n_sel,Kr); dPhi_x=zeros(n_sel,Kr); dPhi_y=zeros(n_sel,Kr);
for k=1:Kr
    m=modes(k,1);n=modes(k,2);
    Phi(:,k)=cos(m*pi*xi_sel).*cos(n*pi*eta_sel);
    dPhi_x(:,k)=-(m*pi/Lx)*sin(m*pi*xi_sel).*cos(n*pi*eta_sel);
    dPhi_y(:,k)=-(n*pi/Ly)*cos(m*pi*xi_sel).*sin(n*pi*eta_sel);
end

% C2b: MFG eval grid
xe=x_min:MFG_EVAL_SPACING:x_max; ye=y_min:MFG_EVAL_SPACING:y_max;
[Xe,Ye]=meshgrid(xe,ye); xi_e=(Xe(:)-x_min)/Lx; eta_e=(Ye(:)-y_min)/Ly;
n_eval=length(xi_e);
dPe_x=zeros(n_eval,Kr);dPe_y=zeros(n_eval,Kr);
d2Pe_xx=zeros(n_eval,Kr);d2Pe_yy=zeros(n_eval,Kr);d2Pe_xy=zeros(n_eval,Kr);
for k=1:Kr
    m=modes(k,1);n=modes(k,2);
    cm=cos(m*pi*xi_e);sm_=sin(m*pi*xi_e);cn_=cos(n*pi*eta_e);sn_=sin(n*pi*eta_e);
    dPe_x(:,k)=-(m*pi/Lx)*sm_.*cn_;
    dPe_y(:,k)=-(n*pi/Ly)*cm.*sn_;
    d2Pe_xx(:,k)=-(m*pi/Lx)^2*cm.*cn_;
    d2Pe_yy(:,k)=-(n*pi/Ly)^2*cm.*cn_;
    d2Pe_xy(:,k)=(m*pi/Lx)*(n*pi/Ly)*sm_.*sn_;
end
fprintf('  Eval grid: %d pts\n', n_eval);

% C3: Median fit
phys_x=zeros(nelx,1);phys_y=zeros(nely,1);
for i=1:nelx,phys_x(i)=grid_data(i,1,1).x;end
for j=1:nely,phys_y(j)=grid_data(1,j,1).y;end
x_fit=[];y_fit=[];z_fit=[];w_fit=[];
for i=1:nelx, for j=1:nely
    if col_has_mat(i,j)
        x_fit(end+1,1)=phys_x(i);y_fit(end+1,1)=phys_y(j);
        z_fit(end+1,1)=z_median_raw(i,j);
        w_fit(end+1,1)=d_sum(i,j)*sqrt(v_cnt(i,j));
    end
end, end
wm=max(w_fit);if wm>0,w_fit=w_fit/wm;end
n_fit=length(z_fit);
xi_fit=(x_fit-x_min)/Lx; eta_fit=(y_fit-y_min)/Ly;
A_fit=zeros(n_fit,Kr);
for k=1:Kr,A_fit(:,k)=cos(modes(k,1)*pi*xi_fit).*cos(modes(k,2)*pi*eta_fit);end
D_curv=diag(curv_weight);
c_geom=(A_fit'*diag(w_fit)*A_fit+0.3*(D_curv'*D_curv))\(A_fit'*diag(w_fit)*z_fit);
fprintf('  Median z=[%.2f, %.2f]\n', min(A_fit*c_geom), max(A_fit*c_geom));

%% ========== C4: COST NORMALIZATION (the critical fix) ==========
fprintf('\n[C4] Cost normalization...\n');
mfg_p=struct('kappa_max',kappa_max,'tan_theta_max',tan_theta_max,...
    'w_concave',MFG_W_CONCAVE,'w_slope',MFG_W_SLOPE,'w_convex_pref',MFG_W_CONVEX_PREF);

% Raw cost at initial point
[~,Ja0,Jg0,Jc0,Jm0]=cost_raw(c_geom,Phi,dPhi_x,dPhi_y,sigma1,w_final,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg_p);

% Raw cost at perturbed point (for reference scale of zero-valued terms)
rng(42);
c_pert=c_geom+(2*rand(Kr,1)-1).*max(abs(c_geom)*0.15,0.5);
[~,Ja_p,Jg_p,Jc_p,Jm_p]=cost_raw(c_pert,Phi,dPhi_x,dPhi_y,sigma1,w_final,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg_p);

% Reference scales
Ja_ref=max([Ja0,Ja_p,1e-6]);
Jg_ref=max([Jg0,Jg_p,1e-6]);
Jc_ref=max([Jc0,Jc_p,1e-6]);
Jm_ref=max([Jm0,Jm_p,1e-6]);

fprintf('  RAW at init:  Ja=%.6f Jg=%.6f Jc=%.6f Jm=%.6f\n', Ja0,Jg0,Jc0,Jm0);
fprintf('  RAW at pert:  Ja=%.6f Jg=%.6f Jc=%.6f Jm=%.6f\n', Ja_p,Jg_p,Jc_p,Jm_p);
fprintf('  Ref scales:   Ja=%.6f Jg=%.6f Jc=%.6f Jm=%.6f\n', Ja_ref,Jg_ref,Jc_ref,Jm_ref);
fprintf('  OLD scale ratio: Ja/Jg=%.0f, Ja/Jm=%.0f <- THIS IS WHY params had no effect\n', ...
    Ja_ref/Jg_ref, Ja_ref/max(Jm_ref,1e-12));

sc=struct('Ja_ref',Ja_ref,'Jg_ref',Jg_ref,'Jc_ref',Jc_ref,'Jm_ref',Jm_ref);

cost_func=@(c) cost_norm(c,Phi,dPhi_x,dPhi_y,sigma1,w_final,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg_p,...
    sc,LAMBDA_ALIGN,LAMBDA_GEOM,LAMBDA_CURV,LAMBDA_MFG);
grad_func=@(c) grad_norm(c,Phi,dPhi_x,dPhi_y,sigma1,w_final,...
    c_geom,curv_weight,dPe_x,dPe_y,d2Pe_xx,d2Pe_yy,d2Pe_xy,mfg_p,...
    sc,LAMBDA_ALIGN,LAMBDA_GEOM,LAMBDA_CURV,LAMBDA_MFG);

[c0_cost,Ja_n,Jg_n,Jc_n,Jm_n]=cost_func(c_geom);
fprintf('\n  NORMALIZED at init:\n');
fprintf('    la*Ja/ref = %.2f*%.4f = %.4f\n', LAMBDA_ALIGN, Ja_n, LAMBDA_ALIGN*Ja_n);
fprintf('    lg*Jg/ref = %.2f*%.4f = %.4f\n', LAMBDA_GEOM, Jg_n, LAMBDA_GEOM*Jg_n);
fprintf('    lc*Jc/ref = %.2f*%.4f = %.4f\n', LAMBDA_CURV, Jc_n, LAMBDA_CURV*Jc_n);
fprintf('    lm*Jm/ref = %.2f*%.4f = %.4f\n', LAMBDA_MFG, Jm_n, LAMBDA_MFG*Jm_n);
fprintf('    TOTAL = %.4f <- all terms now comparable!\n', c0_cost);

% Gradient check
fprintf('\n  Gradient check...\n');
g_a=grad_func(c_geom); g_n=zeros(Kr,1); eps_g=1e-5;
for k=1:min(Kr,5)
    cp=c_geom;cp(k)=cp(k)+eps_g; cm_=c_geom;cm_(k)=cm_(k)-eps_g;
    g_n(k)=(cost_func(cp)-cost_func(cm_))/(2*eps_g);
end
fprintf('    Mode | Analytic | Numerical | Ratio\n');
for k=1:min(Kr,5)
    if abs(g_n(k))>1e-12
        fprintf('    (%d,%d) | %+.5f | %+.5f | %.3f\n',modes(k,1),modes(k,2),g_a(k),g_n(k),g_a(k)/g_n(k));
    else
        fprintf('    (%d,%d) | %+.5f | %+.5f | ~\n',modes(k,1),modes(k,2),g_a(k),g_n(k));
    end
end

%% C5: SA
fprintf('\n[C5] SA (%d DOFs)...\n', Kr);
lb_c=c_geom-Lz*ones(Kr,1); ub_c=c_geom+Lz*ones(Kr,1);
dc_idx=find(modes(:,1)==0&modes(:,2)==0);
if ~isempty(dc_idx),lb_c(dc_idx)=c_geom(dc_idx)-2*Lz;ub_c(dc_idx)=c_geom(dc_idx)+2*Lz;end

best_c=c_geom; best_cost=c0_cost; sa_hist={};
for r=1:SA_RESTARTS
    fprintf('  R%d ',r);
    ci=c_geom+(2*rand(Kr,1)-1).*max(abs(c_geom)*0.3,1.0);
    ci=max(lb_c,min(ub_c,ci));
    cc=ci;cc_cost=cost_func(ci);cb=cc;cb_cost=cc_cost;
    T=SA_T0;ni=0;h=cb_cost;
    while T>1e-4&&ni<SA_NO_IMPROVE
        for in=1:SA_INNER_LOOP
            cn=cc; np=max(1,round(Kr*0.4*rand()));
            for ci_idx=randperm(Kr,np)
                range=ub_c(ci_idx)-lb_c(ci_idx);
                pert=range*0.15*(T/SA_T0)/(1+0.2*curv_weight(ci_idx));
                cn(ci_idx)=max(lb_c(ci_idx),min(ub_c(ci_idx),cc(ci_idx)+(2*rand-1)*pert));
            end
            cn_cost=cost_func(cn); delta=cn_cost-cc_cost;
            if delta<0||rand<exp(-delta/T)
                cc=cn;cc_cost=cn_cost;
                if cn_cost<cb_cost,cb_cost=cn_cost;cb=cn;ni=0;end
            end
            h(end+1)=cb_cost;
        end
        ni=ni+1;T=T*SA_ALPHA;
    end
    sa_hist{r}=h;
    if cb_cost<best_cost,best_cost=cb_cost;best_c=cb;fprintf('-> %.4f\n',cb_cost);
    else,fprintf('(%.4f)\n',cb_cost);end
end
fprintf('  SA best: %.4f\n', best_cost);

%% C6: Adam GD
fprintf('\n[C6] Adam GD (lr=%.3f)...\n', GD_LR);
c_opt=best_c;cost_opt=best_cost;
ma=zeros(Kr,1);va=zeros(Kr,1);b1=0.9;b2=0.999;ea=1e-8;gh=cost_opt;
for it=1:GD_MAX_ITER
    g=grad_func(c_opt);
    ma=b1*ma+(1-b1)*g; va=b2*va+(1-b2)*g.^2;
    mh=ma/(1-b1^it); vh=va/(1-b2^it);
    cn=max(lb_c,min(ub_c,c_opt-GD_LR*mh./(sqrt(vh)+ea)));
    cn_cost=cost_func(cn);
    if cn_cost<cost_opt,cost_opt=cn_cost;best_c=cn;end
    c_opt=cn; gh(end+1)=cost_opt;
    if mod(it,500)==0
        [~,ja_,jg_,jc_,jm_]=cost_func(c_opt);
        fprintf('  it%d cost=%.4f (a=%.3f g=%.3f c=%.3f m=%.3f) |g|=%.4f\n',...
            it,cost_opt,LAMBDA_ALIGN*ja_,LAMBDA_GEOM*jg_,LAMBDA_CURV*jc_,LAMBDA_MFG*jm_,norm(g));
    end
    if it>80&&abs(gh(end)-gh(end-50))<1e-7,fprintf('  Converged it%d\n',it);break;end
end

[final_cost,Ja_f,Jg_f,Jc_f,Jm_f]=cost_func(best_c);
fprintf('\n  Final: %.4f (%.1f%% improvement)\n', final_cost, (c0_cost-final_cost)/c0_cost*100);
fprintf('  Contributions: a=%.3f g=%.3f c=%.3f m=%.3f\n',...
    LAMBDA_ALIGN*Ja_f, LAMBDA_GEOM*Jg_f, LAMBDA_CURV*Jc_f, LAMBDA_MFG*Jm_f);
fprintf('  Coeff change: max=%.2f mm, mean=%.2f mm\n',...
    max(abs(best_c-c_geom)), mean(abs(best_c-c_geom)));

%% C7: Output
fprintf('\n[C7] Output...\n');
res=OUTPUT_RESOLUTION;mg=OUTPUT_MARGIN;
xg=(x_min-mg):res:(x_max+mg); yg=(y_min-mg):res:(y_max+mg);
[Xs,Ys]=meshgrid(xg,yg);
xi_out=(Xs-x_min)/Lx; eta_out=(Ys-y_min)/Ly;

Zs=zeros(size(Xs));dZdx=Zs;dZdy=Zs;d2Zdx2=Zs;d2Zdy2=Zs;d2Zdxdy=Zs;
for k=1:Kr
    m=modes(k,1);n=modes(k,2);
    cm=cos(m*pi*xi_out);sm_=sin(m*pi*xi_out);cn_=cos(n*pi*eta_out);sn_=sin(n*pi*eta_out);
    Zs=Zs+best_c(k)*cm.*cn_;
    dZdx=dZdx+best_c(k)*(-(m*pi/Lx))*sm_.*cn_;
    dZdy=dZdy+best_c(k)*(-(n*pi/Ly))*cm.*sn_;
    d2Zdx2=d2Zdx2+best_c(k)*(-(m*pi/Lx)^2)*cm.*cn_;
    d2Zdy2=d2Zdy2+best_c(k)*(-(n*pi/Ly)^2)*cm.*cn_;
    d2Zdxdy=d2Zdxdy+best_c(k)*((m*pi/Lx)*(n*pi/Ly))*sm_.*sn_;
end

slope_mag=sqrt(dZdx.^2+dZdy.^2); tilt_angle=atand(slope_mag);
denom=1+dZdx.^2+dZdy.^2;
H_mean=((1+dZdy.^2).*d2Zdx2-2*dZdx.*dZdy.*d2Zdxdy+(1+dZdx.^2).*d2Zdy2)./(2*denom.^(1.5));
K_gauss=(d2Zdx2.*d2Zdy2-d2Zdxdy.^2)./denom.^2;
disc=max(0,H_mean.^2-K_gauss);
kappa_concave=max(H_mean+sqrt(disc),0);
in_domain=(Xs>=x_min)&(Xs<=x_max)&(Ys>=y_min)&(Ys<=y_max);

fprintf('  z=[%.2f, %.2f]\n', min(Zs(:)), max(Zs(:)));
fprintf('  Slope max=%.1f deg (lim %.1f)\n', max(tilt_angle(in_domain)), theta_max);
fprintf('  Concave kappa max=%.4f (lim %.4f)\n', max(kappa_concave(in_domain)), kappa_max);
fprintf('  Convex=%.1f%%, Concave=%.1f%%\n',...
    100*sum(H_mean(in_domain)<=0)/sum(in_domain(:)),100*sum(H_mean(in_domain)>0)/sum(in_domain(:)));

Pre_surface={Xs,Ys,Zs};
save('Pre_surface.mat','Pre_surface');
refined_data.spline_surface=struct('type','cosine_mfg_v7c','modes',modes,'coeffs',best_c,'c_geom',c_geom,...
    'x_min',x_min,'x_max',x_max,'y_min',y_min,'y_max',y_max,'Lx',Lx,'Ly',Ly,...
    'Mmax',Mmax,'Nmax',Nmax,'cost',final_cost,'kappa_max_concave',kappa_max,...
    'theta_max',theta_max,'R_min',R_min,'safety_factor',SAFETY_FACTOR,...
    'sym_x',sym_x,'sym_y',sym_y,'scales',sc);
refined_data.surface_params=struct('a',0,'b',0,'c',0,'d',0,'e',0,'f',0,...
    'g',0,'h',best_c(1),'X0',(x_min+x_max)/2,'Y0',(y_min+y_max)/2,...
    'Para_me',0,'type','cosine_mfg','Mmax',Mmax,'Nmax',Nmax);
save('voxel_refined_latest.mat','refined_data','-v7.3');
fprintf('  Saved\n');

